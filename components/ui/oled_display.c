// SSD1104 controller commands
#define SSD1104_DISPLAYOFF          0xAE
#define SSD1104_DISPLAYON           0xAF
#define SSD1104_SETDISPLAYCLOCKDIV  0xD5
#define SSD1104_SETMULTIPLEX        0xA8
#define SSD1104_SETDISPLAYOFFSET    0xD3
#define SSD1104_SETSTARTLINE        0x40
#define SSD1104_CHARGEPUMP          0x8D
#define SSD1104_MEMORYMODE          0x20
#define SSD1104_SEGREMAP            0xA1 // FIX 1: Reverted to 0xA1 to flip the whole screen back
#define SSD1104_COMSCANDEC          0xC8
#define SSD1104_SETCOMPINS          0xDA
#define SSD1104_SETCONTRAST         0x81
#define SSD1104_SETPRECHARGE        0xD9
#define SSD1104_SETVCOMDETECT       0xDB
#define SSD1104_COLUMNADDR          0x21
#define SSD1104_PAGEADDR            0x22
#define SSD1104_SETLOWCOLUMN        0x00
#define SSD1104_SETHIGHCOLUMN       0x10
#define SSD1104_SETPAGESTART        0xB0

static void oled_command(oled_t* oled, uint8_t cmd) {
    gpio_set_level((gpio_num_t)OLED_DC_PIN, 0); // Command mode
    spi_transaction_t t = {
        .length = 8,
        .tx_buffer = &cmd,
    };
    spi_device_transmit(oled->spi, &t);
}

static void oled_data(oled_t* oled, const uint8_t* data, size_t len) {
    gpio_set_level((gpio_num_t)OLED_DC_PIN, 1); // Data mode
    spi_transaction_t t = {
        .length = len * 8,
        .tx_buffer = data,
    };
    spi_device_transmit(oled->spi, &t);
}

esp_err_t oled_init(oled_t* oled) {
    // Configure GPIO pins
    gpio_config_t io_conf = {
        .pin_bit_mask = (1ULL << OLED_RES_PIN) | (1ULL << OLED_DC_PIN),
        .mode = GPIO_MODE_OUTPUT,
    };
    gpio_config(&io_conf);

    // Configure SPI
    spi_bus_config_t buscfg = {
        .mosi_io_num = OLED_MOSI_PIN,
        .sclk_io_num = OLED_CLK_PIN,
        .miso_io_num = -1,
        .quadwp_io_num = -1,
        .quadhd_io_num = -1,
        .max_transfer_sz = OLED_WIDTH * OLED_HEIGHT / 8,
    };

    spi_device_interface_config_t devcfg = {
        .clock_speed_hz = 8 * 1000 * 1000, // 8MHz
        .mode = 0,
        .spics_io_num = OLED_CS_PIN,
        .queue_size = 1,
    };

    spi_bus_initialize(SPI2_HOST, &buscfg, SPI_DMA_CH_AUTO);
    spi_bus_add_device(SPI2_HOST, &devcfg, &oled->spi);

    // Reset display
    gpio_set_level((gpio_num_t)OLED_RES_PIN, 0);
    vTaskDelay(pdMS_TO_TICKS(10));
    gpio_set_level((gpio_num_t)OLED_RES_PIN, 1);
    vTaskDelay(pdMS_TO_TICKS(10));

    // Initialize SSD1104
    oled_command(oled, SSD1104_DISPLAYOFF);
    oled_command(oled, SSD1104_SETDISPLAYCLOCKDIV);
    oled_command(oled, 0x80);
    oled_command(oled, SSD1104_SETMULTIPLEX);
    oled_command(oled, OLED_HEIGHT - 1);
    oled_command(oled, SSD1104_SETDISPLAYOFFSET);
    oled_command(oled, 0x0);
    oled_command(oled, SSD1104_SETSTARTLINE | 0x0);
    oled_command(oled, SSD1104_CHARGEPUMP);
    oled_command(oled, 0x14);  // Enable charge pump
    oled_command(oled, SSD1104_MEMORYMODE);
    oled_command(oled, 0x02);  // Page addressing mode
    // FIX 1: Set segment remap to 0xA1 to scan from right-to-left
    oled_command(oled, SSD1104_SEGREMAP);
    oled_command(oled, SSD1104_COMSCANDEC);
    oled_command(oled, SSD1104_SETCOMPINS);
    oled_command(oled, 0x12);
    oled_command(oled, SSD1104_SETCONTRAST);
    oled_command(oled, 0x8F);
    oled_command(oled, SSD1104_SETPRECHARGE);
    oled_command(oled, 0x22);
    oled_command(oled, SSD1104_SETVCOMDETECT);
    oled_command(oled, 0x35);
    oled_command(oled, SSD1104_DISPLAYON);

    oled_clear(oled);
    oled_display(oled);
    return ESP_OK;
}

void oled_clear(oled_t* oled) {
    memset(oled->buffer, 0, sizeof(oled->buffer));
}

void oled_display(oled_t* oled) {
    for (int page = 0; page < (OLED_HEIGHT / 8); page++) {
        oled_command(oled, SSD1104_SETPAGESTART | page);
        // Add 2-pixel offset for 128x64 SH1106 displays
        oled_command(oled, SSD1104_SETLOWCOLUMN | 0x2);
        oled_command(oled, SSD1104_SETHIGHCOLUMN | 0x0);
        
        const uint8_t* page_data = &oled->buffer[page * OLED_WIDTH];
        oled_data(oled, page_data, OLED_WIDTH);
    }
}

void oled_set_pixel(oled_t* oled, int x, int y, bool on) {
    if (x >= 0 && x < OLED_WIDTH && y >= 0 && y < OLED_HEIGHT) {
        int index = x + (y / 8) * OLED_WIDTH;
        if (on) {
            oled->buffer[index] |= (1 << (y % 8));
        } else {
            oled->buffer[index] &= ~(1 << (y % 8));
        }
    }
}

void oled_print_string(oled_t* oled, int x, int y, const char* str, int size) {
    int start_x = x;
    for (int i = 0; str[i] != '\0'; i++) {
        char c = str[i];
        if (c == '\n') {
            y += 8 * size;
            x = start_x;
            continue;
        }
        
        if (c >= 32 && c <= 126) {
            int font_index = c - 32;
            for (int row = 0; row < 8; row++) {
                uint8_t font_row = font_8x8[font_index][row];
                for (int col = 0; col < 8; col++) {
                    // FIX 2: Read font bits LSB to MSB to flip character horizontally
                    if (font_row & (1 << col)) {
                        for (int sy = 0; sy < size; sy++) {
                            for (int sx = 0; sx < size; sx++) {
                                oled_set_pixel(oled, x + col * size + sx, y + row * size + sy, true);
                            }
                        }
                    }
                }
            }
        }
        x += 8 * size;
    }
}

void oled_print_number(oled_t* oled, int x, int y, int number, int size) {
    char buffer[12];
    snprintf(buffer, sizeof(buffer), "%d", number);
    oled_print_string(oled, x, y, buffer, size);
}

void oled_print_float(oled_t* oled, int x, int y, float number, int decimal_places, int size) {
    char format[10];
    snprintf(format, sizeof(format), "%%.%df", decimal_places);
    
    char buffer[20];
    snprintf(buffer, sizeof(buffer), format, number);
    oled_print_string(oled, x, y, buffer, size);
}

